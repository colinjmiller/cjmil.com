<p>During fall quarter, 2014, I was interviewed by the German department (<a href="http://germanics.washington.edu/news/2015/01/09/undergraduate-student-profiles-meet-colin-miller" target="_blank">department link</a>):</p>
<dl>
  <dt>
    Why did you decide to study German?
  </dt>
  <dd>
    The biggest factor influencing my decision to study German was my grandmother.
    For as long as I can remember, we've had a tradition of playing Gin Rummy together while she tells me stories from her life.
    Her tales of being born in Nazi Germany, hiding in bunkers during air raids, and surviving the post-war era until she emigrated
    to Canada inspired me to learn German so I could talk with her in her native tongue.
  </dd>
  <dt>
    You work in computer science for a company called Whitepages.  Has German been useful to you in your work in this field or in your other studies?
    What are your plans for the future and how will German play a role in helping you achieve your goals?
  </dt>
  <dd>
    So far, my German proficiency has been a great resume item and conversation starter.
    My company, Whitepages, currently only has identity information and support for the United States, but we're working toward internationalizing.
    As an engineer for our public-facing web properties, I will be directly involved with translation and I'm certain my German background will help me
    play a more central role in these internationalization efforts.  Personal projects are a big part of computer science culture, as seen in "Hack-a-thons" and the like.
    After college, I plan to use my German by providing bilingual support for all personal projects I create.
  </dd>
  <dt>
    What is the most interesting thing you have learned in your German classes at UW?
    Is there a time period or cultural movement that particularly interests you?
  </dt>
  <dd>
    I am most interested in the German Democratic Republic.
    This fascination first began by watching "Good-bye, Lenin!" during a German immersion program after my junior year of high school and increases as I take more classes.
    For me, the GDR and soviet-bloc countries are fascinating because they existed so recently, and yet their ideals are so foreign to me.
    I love to hear about successful escapes from communist countries and their reactions to lifestyles in the west.
  </dd>
  <dt>
    Have you been to Germany?  What stood out most about your experience?
  </dt>
  <dd>
    My father served as a pilot in the US Air Force for 28 years, so I lived near Ramstein AFB from the ages of 3-6.
    My fondest memories involved snow, because we lived near a big hill and I loved sledding down it.  I also loved St. Nicholas day because we would find presents in our shoes.
    I recall getting dragged around to a lot of museums and castles as well,
    so I suppose the biggest difference I recall between German and American culture is that Germany simply has more written history.
  </dd>
  <dt>
    Do you have a favorite German musician, composer, artist, or author?  What attracts you to their work?
  </dt>
  <dd>
    My favorite German author is Viktor Frankl, author of "Man's Search for Meaning."
    In the book, Frankl talks about his experiences in various concentration camps, including Auschwitz, and introduces his psychotherapeutic method called logotherapy.
    I am attracted to his work because of his candid telling of concentration camp life and his ideas that the way we perceive the future affects our longevity.
  </dd>
</dl>
<p>
  German has played a large role in my life and I hope it continues after I leave college. I wanted to make good on my comment about my future projects being translated
  into German, so my website has an option at the bottom to change the language.
</p>